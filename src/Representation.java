     private void Remplir(CRepresentation Element, int Rownum){
        Element.Nom = table.getValueAt(Rownum, 1).toString().trim();
        Element.Prenom = table.getValueAt(Rownum,2).toString().trim();
        Element.Genre = table.getValueAt(Rownum, 3).toString().trim();
        String Sup = Objects.toString(table.getModel().getValueAt(Rownum, 4)); 
        Element.Superviseur = ((Sup.equals("null")) ? null : Sup);
    }
    
    private void Equivalence(){
        int Nrow =table.getRowCount();
        
        for (int i=0; i<Nrow;i++){
            CRepresentation First = new CRepresentation();
            Remplir(First,i);
            for (int j=0; j<Nrow;j++){
                if(i==j){continue;}
                CRepresentation Second = new CRepresentation();
                Remplir(Second,j);
                if(First.Equals(Second)) {
                JOptionPane.showMessageDialog(null, "Doublon Detecté!");
                }
            }
        }  
    }
    
