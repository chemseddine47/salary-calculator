    private void CalculerSalaireActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculerSalaireActionPerformed
        CRepresentation Employe = new CRepresentation();
        int row = table.getSelectedRow();
        if (row== -1) {
       JOptionPane.showMessageDialog(null, "Selectionner un Employe!!");
       }
        else {
        Remplir(Employe,row);
        
            if (ChiffreDaffaire.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Entrer un chiffre!!");
            }
            else {
                String nombre = ChiffreDaffaire.getText();
                 double nbr = Double.parseDouble(nombre);

                 double Calc =  Employe.CalculeSalaire(nbr);
                 String Resultat = String.valueOf(Calc);
                 ResulteLabel.setText("<html>le salaire de <br>"+Employe.toString()+" de la Vente <br> est: "+Resultat+" Da </html>");
            }
            }
    }//GEN-LAST:event_CalculerSalaireActionPerformed

